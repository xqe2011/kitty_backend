#!/bin/sh

mysql -uroot -p"${MARIADB_ROOT_PASSWORD}" << EOF
ALTER USER 'root'@'%' REQUIRE SUBJECT '/CN=Root';
RENAME USER '${MARIADB_USER}'@'%' TO '${MARIADB_USER}'@'192.168.0.0/255.255.0.0';
CREATE USER '${MARIADB_USER}'@'172.16.0.0/255.240.0.0' IDENTIFIED BY '${MARIADB_PASSWORD}';
GRANT ALL ON \`${MARIADB_DATABASE}\`.* TO '${MARIADB_USER}'@'172.16.0.0/255.240.0.0' ;
EOF